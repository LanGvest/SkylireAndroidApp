package com.langvest.skylire.utils;

public class Suggestion {

	private String email;
	private String icon;
	private String color;
	private String hash;

	public Suggestion() {}

	public String getEmail() {
		return email;
	}

	public String getIcon() {
		return icon;
	}

	public String getColor() {
		return color;
	}

	public String getHash() {
		return hash;
	}
}